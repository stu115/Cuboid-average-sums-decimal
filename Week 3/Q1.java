package week3;

public class Q1 {
	
	public static void main(String[]args)
	
	//Two integers are being declared and the use of +,-,*,/ and % being demonstrated
	{
		int abi = 8;
		int jenny = 5; 
		
		System.out.println("Adding 8 to 5 is "+(abi+jenny));
		System.out.println("Subtracting 5 from 8 is "+(abi-jenny));
		System.out.println("Multiplying 8 by 5 is "+(abi*jenny));
		System.out.println("The division of 8 by 5 is "+(abi/jenny));
		System.out.println("The remainder of 8%5 is " +(abi%jenny));
	}

}
